/** 실명 뽑기 — 같은 id 는 언제나 같은 이름 (결정론 유지). */
export function realName(cat: RealCategory, id: number): RealName {
  const pool = POOLS[cat]
  const logs = CAT_LOGS[cat]
  const h = (id ^ 0x9e3779b9) >>> 0
  return { name: pool[h % pool.length]!, log: logs[(h >>> 8) % logs.length]! }
}

// ── 등록부 — 태양계·궁수자리 A* 같은 수작업 천체는 여기 이름이 박힌다.
const REGISTRY = new Map<number, RealName>()

export function registerName(id: number, name: string, log: string): void {
  REGISTRY.set(id, { name, log })
}

export function nameOf(id: number): RealName | null {
  return REGISTRY.get(id) ?? null
}
