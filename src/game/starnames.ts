/** 천체 이름 — 「관형어 + 명사」. 같은 시드 = 같은 이름. 이름 없는 티끌의 몫이다. */
export function starName(seed: number): string {
  const r = new Rng(seed ^ 0x5eed)
  return `${HEADS[r.int(HEADS.length)]} ${BODIES[r.int(BODIES.length)]}`
}

/** 관측 기록 한 줄 — 일지에 남는 문장. */
export function starLog(seed: number): string {
  const r = new Rng(seed ^ 0x109)
  return LOGS[r.int(LOGS.length)]!
}

